from django.conf import settings
from urlparse import urljoin

def get_current_path(request):
    return { 'current_path' : request.build_absolute_uri() }

def get_jquery_url(request):
	tmp = urljoin(settings.STATIC_URL, settings.JQUERY_URL)
	return { 'jquery_url' : tmp }



